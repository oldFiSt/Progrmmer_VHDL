module mux_4_1
(           //0001 0010 0100 1000
  input  [3:0] d0, d1, d2, d3,
  input  [1:0] sel, // 00 01 10 11 
  output [3:0] y
);

  
  // Task:
  // Using code for mux_2_1 as an example,
  // write code for 4:1 mux using "?:" operator
  assign y = (2'b00 == sel) ? d0 : 
              (2'b01 == sel) ? d1 : 
              (2'b10 == sel) ? d2 : d3;

              
  

endmodule
